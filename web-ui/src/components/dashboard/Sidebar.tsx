/* Dashboard sidebar navigation. Active state from pathname. Bottom nav on mobile. */
'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { cn } from '@/lib/utils';

const NAV_ITEMS = [
  { href: '/dashboard', label: 'Overview', icon: OverviewIcon },
  { href: '/dashboard/scans', label: 'Scans', icon: ScanIcon },
  { href: '/dashboard/api-keys', label: 'API Keys', icon: KeyIcon },
  { href: '/dashboard/usage', label: 'Usage', icon: ChartIcon },
  { href: '/dashboard/billing', label: 'Billing', icon: CreditCardIcon },
  { href: '/dashboard/profile', label: 'Profile', icon: UserIcon },
] as const;

export function Sidebar() {
  const pathname = usePathname();

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden w-56 shrink-0 border-r border-white/10 bg-[#05070b] lg:block">
        <nav className="flex flex-col gap-1 p-3" aria-label="Dashboard navigation">
          {NAV_ITEMS.map((item) => {
            const active =
              item.href === '/dashboard'
                ? pathname === '/dashboard'
                : pathname.startsWith(item.href);
            const Icon = item.icon;
            return (
              <Link
                key={item.href}
                href={item.href}
                className={cn(
                  'flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors',
                  active
                    ? 'bg-brand-600/10 text-brand-400'
                    : 'text-surface-400 hover:bg-white/[0.04] hover:text-surface-200',
                )}
                aria-current={active ? 'page' : undefined}
              >
                <Icon className="h-4 w-4 shrink-0" />
                {item.label}
              </Link>
            );
          })}
        </nav>
      </aside>

      {/* Mobile bottom nav */}
      <nav
        className="fixed inset-x-0 bottom-0 z-40 flex items-center justify-around border-t border-white/10 bg-[#05070b]/95 backdrop-blur-md lg:hidden"
        aria-label="Dashboard navigation"
      >
        {NAV_ITEMS.slice(0, 5).map((item) => {
          const active =
            item.href === '/dashboard'
              ? pathname === '/dashboard'
              : pathname.startsWith(item.href);
          const Icon = item.icon;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'flex flex-col items-center gap-0.5 px-2 py-2.5 text-[10px] font-medium transition-colors',
                active ? 'text-brand-400' : 'text-surface-500',
              )}
              aria-current={active ? 'page' : undefined}
            >
              <Icon className="h-5 w-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>
    </>
  );
}

